#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	while (*s != '\0')
		s++;
	s--;
	if (c == 0)
		s++;
	while (*s != c)
	{
		if (*s == '\0')
			return (NULL);
		s--;
	}
	return ((char *)s);
}
