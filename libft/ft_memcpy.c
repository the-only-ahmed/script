#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	int			i;
	char		*a_s1;
	const char	*a_s2;

	i = 0;
	a_s1 = s1;
	a_s2 = s2;
	while (n > 0)
	{
		a_s1[i] = a_s2[i];
		n--;
		i++;
	}
	return (a_s1);
}
