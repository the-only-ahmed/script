#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char	*tmp;
	int		i;

	tmp = (char *)s;
	i = 0;
	while (tmp[i] != '\0')
	{
		ft_putchar_fd(tmp[i], fd);
		i++;
	}
}
